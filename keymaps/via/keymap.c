static uint16_t led_timer;
bool            led_layer_state = 0;

// Blink at layer 1, 2 or 3
void matrix_scan_user(void) {
    uint8_t layer   = biton32(layer_state);
    uint8_t usb_led = host_keyboard_leds();

    if (timer_elapsed(led_timer) > LAYER_BLINK_TIME) {
        switch (layer) {
            case 1:
                if (!led_layer_state) {
                    led_set_kb(0);
                } else {
                    led_set_kb(1);
                }

                led_layer_state = !led_layer_state;
                break;
            case 2:
                if (!led_layer_state) {
                    led_set_kb(0);
                } else {
                    led_set_kb(2);
                }

                led_layer_state = !led_layer_state;
                break;
            case 3:
                if (!led_layer_state) {
                    led_set_kb(0);
                } else {
                    led_set_kb(4);
                }

                led_layer_state = !led_layer_state;
                break;
            default:
                led_set_kb(usb_led);
                led_layer_state = 0;
        }

        led_timer = timer_read();
    }
};
